import math

def compute_trigonometric():
    print("Sin of 60 degrees:", math.sin(math.radians(60)))
    print("Cos of pi:", math.cos(math.pi))
    print("Sin of 0.8660254037844386:", math.sin(0.8660254037844386))
    print("Tan of 90 degrees:", math.tan(math.radians(90)))

compute_trigonometric()